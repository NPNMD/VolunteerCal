import { useState } from 'react';
import { Link } from 'react-router-dom';
import { Mail, Lock, Eye, EyeOff } from 'lucide-react';
import { useAuth } from '@/hooks/useAuth';
import { validateEmail, validatePassword } from '@/utils/validators';

export function LoginForm() {
  const { signIn, signInWithGoogle, loading } = useAuth();
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [error, setError] = useState('');

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError('');

    const emailErr = validateEmail(email);
    const passErr = validatePassword(password);
    if (emailErr) { setError(emailErr); return; }
    if (passErr) { setError(passErr); return; }

    try {
      await signIn(email, password);
    } catch (err: unknown) {
      setError(err instanceof Error ? err.message : 'Failed to sign in');
    }
  };

  return (
    <div className="w-full max-w-md space-y-8">
      <div className="text-center">
        <h2 className="text-3xl font-bold text-charcoal-900" style={{color: '#2C2825'}}>Welcome back</h2>
        <p className="mt-2 text-warm-gray-600" style={{color: '#8B8480'}}>Sign in to your account</p>
      </div>

      {error && (
        <div className="rounded-xl p-4 text-sm" style={{backgroundColor: '#FDF5F2', color: '#E76F51'}}>
          {error}
        </div>
      )}

      <form onSubmit={handleSubmit} className="space-y-5">
        <div>
          <label className="mb-1 block text-sm font-medium text-charcoal-900" style={{color: '#2C2825'}}>Email</label>
          <div className="relative">
            <Mail className="absolute left-3 top-1/2 h-5 w-5 -translate-y-1/2" style={{color: '#9E9690'}} strokeWidth={2.5} />
            <input
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              className="w-full rounded-xl border py-2.5 pl-10 pr-4 text-sm focus:outline-none focus:ring-2"
              style={{
                backgroundColor: '#FAF8F5',
                borderColor: '#D9D5D0',
                color: '#2C2825',
              }}
              placeholder="you@example.com"
            />
          </div>
        </div>

        <div>
          <label className="mb-1 block text-sm font-medium text-charcoal-900" style={{color: '#2C2825'}}>Password</label>
          <div className="relative">
            <Lock className="absolute left-3 top-1/2 h-5 w-5 -translate-y-1/2" style={{color: '#9E9690'}} strokeWidth={2.5} />
            <input
              type={showPassword ? 'text' : 'password'}
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className="w-full rounded-xl border py-2.5 pl-10 pr-10 text-sm focus:outline-none focus:ring-2"
              style={{
                backgroundColor: '#FAF8F5',
                borderColor: '#D9D5D0',
                color: '#2C2825',
              }}
              placeholder="Enter your password"
            />
            <button type="button" onClick={() => setShowPassword(!showPassword)} className="absolute right-3 top-1/2 -translate-y-1/2 hover:text-warm-gray-700" style={{color: '#9E9690'}}>
              {showPassword ? <EyeOff className="h-5 w-5" strokeWidth={2.5} /> : <Eye className="h-5 w-5" strokeWidth={2.5} />}
            </button>
          </div>
        </div>

        <button
          type="submit"
          disabled={loading}
          className="w-full rounded-xl py-3 text-sm font-semibold text-white transition-colors active:scale-[0.98] disabled:opacity-50 sm:py-2.5 bg-coral-500 hover:bg-coral-600"
          style={{backgroundColor: loading ? '#FFB5AD' : '#FF6B5A'}}
        >
          {loading ? 'Signing in...' : 'Sign In'}
        </button>
      </form>

      <div className="relative">
        <div className="absolute inset-0 flex items-center"><div className="w-full border-t" style={{borderColor: '#D9D5D0'}} /></div>
        <div className="relative flex justify-center text-sm"><span className="px-2" style={{backgroundColor: '#FAF8F5', color: '#9E9690'}}>or</span></div>
      </div>

      <button
        onClick={signInWithGoogle}
        className="flex w-full items-center justify-center gap-2 rounded-xl border py-3 text-sm font-medium transition-colors hover:bg-cream-100 active:bg-cream-100 sm:py-2.5"
        style={{borderColor: '#D9D5D0', color: '#5C5854'}}
      >
        <svg className="h-5 w-5" viewBox="0 0 24 24"><path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4"/><path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/><path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/><path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/></svg>
        Continue with Google
      </button>

      <p className="text-center text-sm" style={{color: '#8B8480'}}>
        Don't have an account?{' '}
        <Link to="/register" className="font-medium text-coral-500 hover:text-coral-600" style={{color: '#FF6B5A'}}>Sign up</Link>
      </p>
    </div>
  );
}
